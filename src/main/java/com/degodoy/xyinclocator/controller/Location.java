package com.degodoy.xyinclocator.controller;

public class Location {
	
	int x;
	int y;
	int dMax;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getdMax() {
		return dMax;
	}
	public void setdMax(int dMax) {
		this.dMax = dMax;
	}
	
	

}
